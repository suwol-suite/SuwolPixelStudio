const resources: Record<ResolvedLanguage, Record<TranslationKey, string>> = {
  en,
  ko,
};

export type Translate = (key: string) => string;

export function createTranslator(language: ResolvedLanguage): Translate {
  const primary: Readonly<Record<string, string>> = resources[language];
  const fallback: Readonly<Record<string, string>> = en;
  return (key) => primary[key] ?? fallback[key] ?? key;
}
